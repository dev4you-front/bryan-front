// Fonction utilitaire pour filtrer les formations par type
export const getFormationsByType = (type: 'neuro' | 'sport'): UpcomingFormation[] => {
  return upcomingFormations.filter(formation => formation.type === type);
};

// Fonction utilitaire pour obtenir les prochaines formations (limitées à un nombre donné)
export const getUpcomingFormations = (limit?: number): UpcomingFormation[] => {
  const now = new Date();
  const upcoming = upcomingFormations
    .filter(formation => new Date(formation.date) >= now)
    .sort((a, b) => new Date(a.date).getTime() - new Date(b.date).getTime());
  
  return limit ? upcoming.slice(0, limit) : upcoming;
};

// Fonction utilitaire pour obtenir les formations par pays
export const getFormationsByCountry = (country: string): UpcomingFormation[] => {
  return upcomingFormations.filter(formation => formation.country === country);
};